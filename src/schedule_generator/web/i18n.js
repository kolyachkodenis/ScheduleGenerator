const catalogs = { en: {}, ru: RU };
const attributeSources = new WeakMap();
let language = localStorage.getItem("schedule-generator-language") || "en";

function t(key, values = {}) {
  const template = catalogs[language]?.[key] || key;
  return template.replace(/\{\{(\w+)\}\}/g, (_match, name) => String(values[name] ?? ""));
}

function translateStatic(root = document.body) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  let node;
  while ((node = walker.nextNode())) {
    if (["SCRIPT", "STYLE", "TEXTAREA"].includes(node.parentElement?.tagName)) continue;
    const source = node.__i18nSource ?? node.nodeValue;
    node.__i18nSource = source;
    const value = source.trim();
    if (!value) continue;
    node.nodeValue = source.replace(value, t(value));
  }
  root.querySelectorAll("[title], [aria-label], [placeholder]").forEach((element) => {
    const sources = attributeSources.get(element) || {};
    for (const attribute of ["title", "aria-label", "placeholder"]) {
      if (!element.hasAttribute(attribute)) continue;
      sources[attribute] ??= element.getAttribute(attribute);
      element.setAttribute(attribute, t(sources[attribute]));
    }
    attributeSources.set(element, sources);
  });
}

function setLanguage(nextLanguage, notify = true) {
  language = nextLanguage === "ru" ? "ru" : "en";
  localStorage.setItem("schedule-generator-language", language);
  document.documentElement.lang = language;
  document.querySelectorAll("[data-language]").forEach((button) => {
    const active = button.dataset.language === language;
    button.classList.toggle("active", active);
    button.setAttribute("aria-pressed", String(active));
  });
  translateStatic();
  if (notify) window.dispatchEvent(new CustomEvent("schedule-language-change"));
}

function translateError(message) {
  const translated = t(message);
  if (translated !== message) return translated;
  const patterns = [
    [/^password must be at least (\d+) characters$/, "Пароль должен содержать не менее $1 символов"],
    [/^account is locked until (.+)$/, "Учётная запись заблокирована до $1"],
    [/^permission (.+) is required$/, "Требуется разрешение $1"],
    [/^user (.+) not found$/, "Пользователь $1 не найден"],
    [/^unknown role (.+)$/, "Неизвестная роль $1"],
    [/^role (.+) cannot perform this action$/, "Роль $1 не может выполнить это действие"],
    [/^job (.+) is not pending$/, "Запуск $1 не находится в ожидании"],
    [/^job (.+) could not be claimed$/, "Не удалось начать запуск $1"],
    [/^unknown period (.+)$/, "Неизвестный урок $1"],
    [/^regeneration failed: (.+)$/, "Не удалось пересоставить расписание: $1"],
    [/^Teacher (.+) non-preferred start$/, "Начало урока учителя $1 вне предпочтительного времени"],
    [/^Difficult workload above target for (.+) on (.+)$/, "Сложная нагрузка выше целевой для $1 в день $2"],
    [/^Daily load spread for (.+)$/, "Неравномерная дневная нагрузка для $1"],
    [/^Internal class gap for (.+) on (.+) period (\d+)$/, "Окно у класса $1 в день $2, урок $3"],
    [/^Internal teacher gap for (.+) on (.+) period (\d+)$/, "Окно у учителя $1 в день $2, урок $3"],
    [/^Repeated (.+) starts for (.+) on (.+)$/, "Повтор предмета $1 у $2 в день $3"],
    [/^Unpaired (.+) and (.+) for (.+) on (.+)$/, "Предметы $1 и $2 не совмещены у $3 в день $4"]
  ];
  const pattern = patterns.find(([expression]) => expression.test(message));
  return language === "ru" && pattern ? message.replace(pattern[0], pattern[1]) : message;
}

